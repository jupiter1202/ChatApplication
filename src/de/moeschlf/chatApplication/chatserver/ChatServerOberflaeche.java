    /**
     * This method is called from the startbutton actionhandler to start a Thread, which updates the clientsTextArea and the messagesTextArea.
     */
    private void update(){
        new Thread(new Runnable() {
            
            @Override
            public void run() {
                while(true){
                    clientsTextArea.setText(chatServer.clientstoString());
                    messagesTextArea.setText(chatServer.messagesToString());
                    try{
                        Thread.sleep(500);

                        } catch (InterruptedException ex) {
                            ex.printStackTrace();
                        }
                    }
                }
        }).start();
    }
